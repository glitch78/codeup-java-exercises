import javax.print.DocFlavor;

public class StringExercise {
    public static void main(String[] args) {
        String message = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(message);
        String message2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(message2);
        String message3 = "In windows, the main drive is usually c:\\";
        System.out.println(message3);

        String message4 = "I can do backslashes \\, double backslashes \\\\ and the amazing triple backslashes \\\\\\";
        System.out.println(message4);
    }
}

